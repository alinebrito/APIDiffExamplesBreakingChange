package br.ufmg.dcc.labsoft.fields;

public class Field1 {

	//public --> private
	public int x1;
	
	//public --> protected
	public int x2;
	
	//public --> default
	public int x3;
	
	
	//private --> protected
	private int x4;
	
	//private --> default
	private int x5;
	
	//private --> public
	private int x6;
	
	
	//protected --> private
	protected int x7;
	
	//protected --> default
	protected int x8;
	
	//protected --> public
	protected int x9;
	
	
	//default --> private
	int x10;
	
	//default --> protected
	int x11;
	
	//default --> public
	int x12;
	
}
